package ejercicio_tema_1;

public class tipo_de_datos {
    public static void main(String[] args) {
        int valor_0 = 999999999;
        long valor_1 = 999999999999999999L;
        double valor_2 = 999999d;
        boolean valor_3 = true;
        String valor_4 = "Primer ejercicio";
        //Imprimir
        System.out.println("Int = " + valor_0);
        System.out.println("Long = " + valor_1);
        System.out.println("Double = " + valor_2);
        System.out.println("Boolean = " + valor_3);
        System.out.println("String = " + valor_4);
    }
}
